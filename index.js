// Given an integer array nums where the elements are sorted in ascending order, convert it to a
// height-balanced
//  binary search tree.  Input: nums = [-10,-3,0,5,9]
// Output: [0,-3,9,-10,null,5]
// Explanation: [0,-10,5,null,-3,null,9] is also accepted:  Input: nums = [1,3]
// Output: [3,1]
// Explanation: [1,null,3] and [3,1] are both height-balanced BSTs.

// Constraints:

// 1 <= nums.length <= 104
// -104 <= nums[i] <= 104
// nums is sorted in a strictly increasing order. 

var sortedArrayToBST = function (nums) {
  class TreeNode {
    constructor(val, left = null, right = null) {
      this.val = val;
      this.left = left;
      this.right = right;
    }
  }

  // Helper function to recursively build the BST
  function buildTree(left, right) {
    if (left > right) {
      return null; // Base case: no elements to form a subtree
    }

    const mid = Math.floor((left + right) / 2); // Choose the middle element
    const root = new TreeNode(nums[mid]); // Create the root node

    // Recursively build left and right subtrees
    root.left = buildTree(left, mid - 1);
    root.right = buildTree(mid + 1, right);

    return root;
  }

  return buildTree(0, nums.length - 1); // Start with the entire array
};